lista_mercado = []

lista_mercado.append("banana")  # Adiciona uva ao final da lista

lista_mercado.remove("banana")  # Remove banana da lista

lista_mercado.pop(2)  # Remove o item no índice 2 (laranja)

print(lista_mercado.index("laranja"))  # Mostra o índice de laranja na lista

print(lista_mercado.count("pera"))  # Conta quantas vezes pera aparece na lista

lista_mercado.sort()  # Ordena a lista em ordem alfabética

print(lista_mercado.reverse())  # Inverte a ordem da lista

print(len(lista_mercado))  # Mostra o tamanho da listama = "maçã"
